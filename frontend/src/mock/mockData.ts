export const createPaginatedResponse = <T>(
  list: T[],
  page: number = 1,
  size: number = 10
): { records: T[]; total: number; size: number; current: number; pages: number } => {
  const start = (page - 1) * size;
  const end = start + size;
  const records = list.slice(start, end);
  const total = list.length;
  const pages = Math.ceil(total / size);

  return {
    records,
    total,
    size,
    current: page,
    pages
  };
};

export const filterList = <T>(
  list: T[],
  filters: Record<string, string | undefined>,
  searchField: keyof T,
  categoryField?: keyof T,
  statusField?: keyof T
): T[] => {
  return list.filter(item => {
    const keyword = filters.name || filters.title || '';
    const category = filters.category || '';
    const status = filters.status || '';

    let matches = true;

    if (keyword) {
      const value = String(item[searchField] || '').toLowerCase();
      matches = matches && value.includes(keyword.toLowerCase());
    }

    if (category && categoryField) {
      matches = matches && String(item[categoryField]) === category;
    }

    if (status && statusField) {
      matches = matches && String(item[statusField]) === status;
    }

    return matches;
  });
};
